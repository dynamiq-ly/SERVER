<?php

namespace App\Http\Controllers\room;

use App\Http\Controllers\Controller;
use App\Models\room\RoomsListFeatures;
use Illuminate\Http\Request;

class RoomFeaturesController extends Controller
{
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {
        $qeury = $request->input('query');

        if ($qeury !== null) {
            return RoomsListFeatures::where('room_id', $qeury)->get();
        }
        return RoomsListFeatures::all();
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        return RoomsListFeatures::create($request->all());
    }

    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        return RoomsListFeatures::find($id);
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        return RoomsListFeatures::find($id)->update($request->all());
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        return RoomsListFeatures::destroy($id);
    }
}
